const ResponseBody = require('../../shared/ResonseBody.model.js');
const { createCompras, viewCompras, updateCompras, deleteCompras} = require('../controller/compras.controller');

const createComprasAPI = async (req, res) => {
  let { usuarios_id, productos_id} = req.body;
  let message;

  try {
    let response = await createCompras({ usuarios_id, productos_id});
    message = new ResponseBody(true, 200, response);
  } catch (error) {
    if (error.statusCode) {
      message = new ResponseBody(error.ok, error.statusCode, error.data);
    } else {
      console.log(error);
      message = new ResponseBody(false, 500, 'Ocurri贸 un error al procesar la solicitud.');
    }
  }

  return res.json(message);
}

